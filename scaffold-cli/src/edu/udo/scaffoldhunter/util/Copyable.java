package edu.udo.scaffoldhunter.util;

import java.io.ByteArrayInputStream;
import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.io.ObjectInputStream;
import java.io.ObjectOutputStream;
import java.io.Serializable;

/**
 * @author Thorsten Fl√ºgel
 *
 */
public class Copyable implements Serializable {
    /**
     * Makes a deep copy of this object.
     * 
     * This means that all members are also copied, so that the original object
     * and the copy don't refer to the same member objects, as it would be in
     * case of a shallow copy.
     *  
     * @return  a copy of this object
     */
    public Copyable copy() {
        Copyable obj = null;
        try {
            // Write the object out to a byte array
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            ObjectOutputStream out = new ObjectOutputStream(bos);
            out.writeObject(this);
            out.flush();
            out.close();
    
            // Make an input stream from the byte array and read
            // a copy of the object back in.
            ObjectInputStream in = new ObjectInputStream(
                    new ByteArrayInputStream(bos.toByteArray()));
            obj = (Copyable) in.readObject();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException cnfe) {
            cnfe.printStackTrace();
        }
        return obj;
    }

}